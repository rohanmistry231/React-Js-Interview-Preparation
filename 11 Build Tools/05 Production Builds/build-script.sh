# Main Learning Points: Production Builds
# Production builds optimize your app for deployment by minifying code and removing development features.

# For Create React App
npm run build
# Output will be in the 'build' directory

# For Vite
npm run build
# Output will be in the 'dist' directory

# For Webpack
npm run build
# Output will be in the 'dist' directory as specified in webpack.config.js